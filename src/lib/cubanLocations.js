/**
 * Get municipalities for a given province
 */
export const getMunicipalitiesByProvince = (provinceName) => {
  const province = cubanProvinces.find(p => p.name === provinceName);
  return province ? province.municipalities : [];
};

/**
 * Get all province names
 */
export const getProvinceNames = () => {
  return cubanProvinces.map(p => p.name);
};
